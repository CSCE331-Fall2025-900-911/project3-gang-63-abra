const TRANSLATIONS = Object.keys(baseTranslations).reduce((acc, lang) => {
  acc[lang] = { ...baseTranslations[lang] };
  // Merge drink/topping names
  Object.entries(drinkAndToppingNames).forEach(([name, langs]) => {
    if (langs[lang]) {
      acc[lang][name] = langs[lang];
    }
  });
  return acc;
}, {});

export const translate = (text, lang) => TRANSLATIONS[lang]?.[text] || text;
